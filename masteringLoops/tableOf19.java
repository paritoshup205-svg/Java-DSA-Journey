package Java_Dsa.masteringLoops;

import java.util.Scanner;

public class tableOf19 {
    public static void main(String[] args) {
        Scanner ss = new Scanner(System.in);
        System.out.println("Enter n :");
        int n= ss.nextInt();
        for(int i=1;i<=10;i++){
                System.out.println(n*i);
            }
        }
    }